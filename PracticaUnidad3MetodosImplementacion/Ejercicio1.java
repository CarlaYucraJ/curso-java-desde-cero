
/**
 * Este programa calcula el tiempo que falta para que empiece la primavera.
 *
 * @author (CarlaY)
 * @version (1.0)
 */
public class Ejercicio1
{
   public int tiempoQueFalta(int hora19Sep){
       int tiempoRes = 24-hora19Sep;
       int totalHoras = tiempoRes+24;
       return totalHoras;
    }
}

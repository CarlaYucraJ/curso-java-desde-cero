
/**
 * Este programa calcula el perimetro de una circunferencia.
 *
 * @author (CarlaY)
 * @version (1.0)
 */
public class Ejercicio3
{
    public double perimetroCirc(int radio){
        double pi=3.14159;
        double perimetro;
        perimetro=(2*radio)*pi;
        return perimetro;
    }
}
